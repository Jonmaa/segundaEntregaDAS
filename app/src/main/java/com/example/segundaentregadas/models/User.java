package com.example.segundaentregadas.models;

public class User {
    private int id;
    private String nombre;
    private String email;
    private String foto_url;

    // Getters
    public int getId() {
        return id;
    }

    public String getNombre() {
        return nombre;
    }

    public String getEmail() {
        return email;
    }

    public String getFotoUrl() {
        return foto_url;
    }
}